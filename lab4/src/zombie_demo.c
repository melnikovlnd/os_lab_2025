#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>

int main() {
    printf("Создаю зомби-процесс...\n");
    
    // Создаем дочерний процесс
    if (fork() == 0) {
        // Это дочерний процесс
        printf("Дочерний: я завершаюсь и становлюсь зомби!\n");
        exit(0); // Завершаемся
    }
    
    // Это родительский процесс
    printf("Родитель: я НЕ убираю за дочерним процессом!\n");
    printf("Зомби появится на 30 секунд...\n");
    
    // Родитель спит и НЕ убирает за дочерним процессом
    sleep(10);
    
    printf("Родитель завершился - зомби исчез!\n");
    return 0;
}